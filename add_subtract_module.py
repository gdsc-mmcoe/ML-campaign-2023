def add_two_numbers(a : int, b : int) -> int:
    """Adds two integers together.

    Args:
        a (int): first integer
        b (int): second integer

    Returns:
        int: addition of the two integers a and b
    """
    return a + b

def subtract_two_numbers(a : int, b : int) -> int:
    """Subtracts two integers.

    Args:
        a (int): first integer
        b (int): second integer

    Returns:
        int: subtraction of the two integers a and b
    """
    return a - b